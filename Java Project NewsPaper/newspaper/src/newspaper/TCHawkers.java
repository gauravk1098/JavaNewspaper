package newspaper;


public class TCHawkers 
{
	String  Hname,Mobile,Area,Adress;

	public TCHawkers(String hname, String mobile, String area, String adress) {
		super();
		Hname = hname;
		Mobile = mobile;
		Area = area;
		Adress = adress;
	}

	public String getHname() {
		return Hname;
	}

	public void setHname(String hname) {
		Hname = hname;
	}

	public String getMobile() {
		return Mobile;
	}

	public void setMobile(String mobile) {
		Mobile = mobile;
	}

	public String getArea() {
		return Area;
	}

	public void setArea(String area) {
		Area = area;
	}

	public String getAdress() {
		return Adress;
	}

	public void setAdress(String adress) {
		Adress = adress;
	}
	

}
